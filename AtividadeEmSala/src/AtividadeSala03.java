public class AtividadeSala03 {
    public static void main(String[] args){
        int a = 15;
        int b = 20;
        int resutado = 0;

        resutado = a + b;
        System.out.println("a + b =" + resutado);
        resutado = a - b;
        System.out.println("a - b =" + resutado);
        resutado = a * b;
        System.out.println("a * b =" + resutado);
        resutado = a / b;
        System.out.println("a / b =" + resutado);

        System.out.println("a == b ?" + (a == b));
        System.out.println("a != b ?" + (a != b));
        System.out.println("a >= b ?" + (a >= b));
        System.out.println("a =< b ?" + (a <= b));

    }
}
